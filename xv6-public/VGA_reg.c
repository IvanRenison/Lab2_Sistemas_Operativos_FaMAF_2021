void
vgaSetPalette(int index, int r, int g, int b)
{
  outb(VGA_DAC_WRITE_INDEX, index);
  outb(VGA_DAC_DATA, r);
  outb(VGA_DAC_DATA, g);
  outb(VGA_DAC_DATA, b);
}

/* Seetea la paleta del VGA al default */
void
setdefaultVGApalette()
{
  for(int index = 0; index < 256; index++){
    int value = VGA_palette_256_24[index];
    vgaSetPalette(index,
              (value>>18)&0x3f,
              (value>>10)&0x3f,
              (value>>2)&0x3f);
  }
}


bool
mode_is_text(VGA_mode mode)
{
  return(VGA_text_40x25 <= mode && mode <= VGA_text_90x60);
}

bool
mode_is_graphic(VGA_mode mode)
{
  return(VGA_graphic_640x480x2 <= mode && mode <= VGA_graphic_320x200x256_modex);
}

/* Retorna el ancho del modo, tanto si es modo texto como gráfico
 * si en un modo invalido retorna 0
 */
int
mode_width(VGA_mode mode)
{
  int res = 0;
  switch(mode){
  case VGA_text_40x25:
  case VGA_text_40x50:
    res = 40;
    break;
  case VGA_text_80x25:
  case VGA_text_80x50:
    res = 80;
    break;
  case VGA_text_90x30:
  case VGA_text_90x60:
    res = 90;
    break;
  case VGA_graphic_640x480x2:
    res = 640;
    break;
  case VGA_graphic_320x200x4:
    res = 320;
    break;
  case VGA_graphic_640x480x16:
    res = 640;
    break;
  case VGA_graphic_720x480x16:
    res = 720;
    break;
  case VGA_graphic_320x200x256:
  case VGA_graphic_320x200x256_modex:
    res = 320;
    break;
  }
  return(res);
}

/* Retorna la altura del modo, tanto si es modo texto como gráfico
 * si en un modo invalido retorna 0
 */
int
mode_height(VGA_mode mode)
{
  int res = 0;
  switch(mode){
  case VGA_text_40x25:
  case VGA_text_80x25:
    res = 25;
    break;
  case VGA_text_40x50:
  case VGA_text_80x50:
    res = 50;
    break;
  case VGA_text_90x30:
    res = 30;
    break;
  case VGA_text_90x60:
    res = 60;
    break;
  case VGA_graphic_640x480x2:
  case VGA_graphic_640x480x16:
  case VGA_graphic_720x480x16:
    res = 480;
    break;
  case VGA_graphic_320x200x4:
  case VGA_graphic_320x200x256:
  case VGA_graphic_320x200x256_modex:
    res = 200;
    break;
  }
  return(res);
}

/* Variable para saber el modo actual
 * Cada ves que se cambia de modo se la actualiza
 */
VGA_mode actual_mode = VGA_text_80x25;

static void
VGA_text_plot_letter(int x, int y, char letter, char atributes)
{
  int width = mode_width(actual_mode);
  int height = mode_height(actual_mode);
  VGA_char* charbuffer = P2V(get_fb_seg());
  if(0 <= x && x < width && 0 <= y && y < height) {
    charbuffer[x + y*width] = (VGA_char){letter, atributes};
  }
}

static void
VGA_text_put_string(int x, int y, char* str, char atributes)
{
  if(str != NULL) {
    for(uint i = 0u; str[i] != '\0'; i++) {
      VGA_text_plot_letter(x, y, str[i], atributes);
      x++;
    }
  }
}

/* Si el modo actual es de texto dibuja un pie de pantalla que dice SO2021
 */
void
vgainit(void)
{
  if(mode_is_text(actual_mode)){
    // Pintar el fondo
    int width = mode_width(actual_mode);
    for (int x = 0; x < width; x++) {
      VGA_text_plot_letter(x, 0, ' ', 0x20);
    }

    VGA_text_put_string(width/2 - 3, 0, "SO2021", 0x2f);
  }
}


/* Llamada al sistema que cambia de modo al modo dado,
 * si es un modo invalido no hace nada
 *
 * Cosas a tener en cuenta:
 *   La memoría no se inicializa, por lo cuál al cambiar de modo hay basura
 *   La fuente g_8x16_font no se ve bien para todos los modos de texto
 */
void
VGA_mode_switch(VGA_mode mode)
{
  if(mode_is_text(mode)){
    // Cambio de modo
    write_regs(VGA_modes[mode]);
    actual_mode = mode;

    // Se ponen las fuentes
    write_font(g_8x16_font, 16);

    // Se inicializa todo
    int rows = mode_height(mode);
    int cols = mode_width(mode);
    for(int i = 0; i < cols; i++){
      for(int j = 0; j < rows; j++){
        VGA_text_plot_letter(i, j, ' ', 0x00);
      }
    }

    // Se llama a vgainit para que aparezca el pie de pantalla
    vgainit();
  }
  else if(mode_is_graphic(mode)){
    write_regs(VGA_modes[mode]);
    actual_mode = mode;
    setdefaultVGApalette();
  }
}

/* Si las cordenadas son dentro de la pantalla (de 320x200) pinta el pixel,
 * si están afuera no hace nada.
 *
 * Si no está en modo gráfico no hace nada
 */
void
VGA_plot_pixel(int x, int y, uchar color)
{
  int width = mode_width(actual_mode);
  int height = mode_height(actual_mode);

  if(0 <= x && x < width && 0 <= y && y < height
    && mode_is_graphic(actual_mode)){

    uchar* framebuffer = P2V(get_fb_seg());
    framebuffer[y * width + x] = color;
  }
}

/* Copia mode_width(actual_mode)*mode_height(actual_mode) bytes de buffer+
 * al arreglo de la pantalla
 *
 * Si no se está en modo gráfico no hace nada
 */
void
VGA_plot_screen(uchar* buffer)
{
  if(mode_is_graphic(actual_mode)){
    int width = mode_width(actual_mode);
    int height = mode_height(actual_mode);
    uchar* framebuffer = P2V(get_fb_seg());

    for(int j = 0; j < width*height; j++){
      framebuffer[j] = buffer[j];
    }
  }
}


int
sys_VGA_mode_switch(void)
{
  int mode;
  if(argint(0, &mode) < 0)
    return -1;
  VGA_mode_switch((VGA_mode)mode);
  return 0;
}

int
sys_VGA_plot_pixel(void)
{
  int x;
  int y;
  uchar color;
  if(argint(0, &x) < 0)
    return -1;
  if(argint(1, &y) < 0)
    return -1;
  int temp_color;
  if(argint(2, &temp_color) < 0)
    return -1;
  color = (char)temp_color;
  VGA_plot_pixel(x, y, color);
  return 0;
}

int
sys_VGA_plot_screen(void)
{
  char* buffer;
  if(argptr(0, &buffer, mode_width(actual_mode)*mode_height(actual_mode)) < 0)
    return -1;
  VGA_plot_screen((uchar*)buffer);
  return 0;
}


